# Boolean
# Boolean mewakili salah satu dari dua nilai: Benar (True) atau Salah (False)

# Boolean Values
# In programming you often need to know if an expression is True or False.
# You can evaluate any expression in Python, and get one of two answers, True or False.

print(10 > 9)
print(10 == 9)
print(10 < 9)

# ======

a = 200
b = 33

if b > a:
  print("b is greater than a")
else:
  print("b is not greater than a")

# Evaluate Values and Variables

# Fungsi bool() memungkinkan Anda untuk mengevaluasi nilai apa pun, dan memberi Anda True atau False sebagai imbalannya

print(bool("Hello"))
print(bool(15))

# ====

x = "Hello"
y = 15

print(bool(x))
print(bool(y))

